--TEST DE GET_TICKET
CREATE TABLE TEMP_TABLE AS
SELECT * FROM GLPI_CERGY.GLOBAL_Ticket WHERE 1 = 0; 
/
DECLARE
    v_ticket GLPI_CERGY.GLOBAL_Ticket%ROWTYPE;
BEGIN
    v_ticket := GLPI_CERGY.GET_TICKET(1);
    INSERT INTO TEMP_TABLE VALUES v_ticket;
END;
/
SET SERVEROUTPUT ON;
/
SELECT * FROM TEMP_TABLE;
/
SET SERVEROUTPUT OFF;
/

--TEST DE GLPI_CERGY.GET_USER_TICKETS
CREATE TABLE TEMP_TABLE2 (ticket_id INT);
DECLARE
    ticket_id_list SYS.ODCINUMBERLIST;
    i INT;
BEGIN
    ticket_id_list := GLPI_CERGY.GET_USER_TICKETS(1);
    
    FOR i IN 1..ticket_id_list.COUNT LOOP
        INSERT INTO TEMP_TABLE2 VALUES (ticket_id_list(i));
    END LOOP;
END;
/
SET SERVEROUTPUT ON;
/
SELECT * FROM TEMP_TABLE2;
/
SET SERVEROUTPUT OFF;
/

/

